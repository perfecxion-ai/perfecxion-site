function extractTagsFromContent(content) {
  const text = `${content.title || ''} ${content.description || ''} ${content.excerpt || ''}`.toLowerCase();
  const tags = new Set();
  
  // Check each keyword
  for (const [keyword, keywordTags] of Object.entries(tagMappings)) {
    if (text.includes(keyword.toLowerCase())) {
      keywordTags.forEach(tag => tags.add(tag));
    }
  }
  
  // Limit to 7 most relevant tags
  return Array.from(tags).slice(0, 7);
}

function updateContentFile(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf-8');
    const { data, content: mdxContent } = matter(content);
    
    // Extract tags based on content
    const extractedTags = extractTagsFromContent(data);
    
    // Merge with existing tags if any
    const existingTags = data.tags || [];
    const allTags = [...new Set([...existingTags, ...extractedTags])];
    
    // Update frontmatter
    data.tags = allTags.slice(0, 7); // Keep max 7 tags
    
    // Write back
    const newContent = matter.stringify(mdxContent, data);
    fs.writeFileSync(filePath, newContent);
    
    console.log(`Updated ${path.basename(filePath)}: ${data.tags.join(', ')}`);
  } catch (error) {
    console.error(`Error updating ${filePath}:`, error.message);
  }
}

